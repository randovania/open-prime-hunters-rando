@pytest.fixture(scope="module", params=all_camera_sequences)
def camera_sequence_file(rom, request):
    return rom.getFileByName(f"cameraEditor/{request.param}.bin")


def test_compare_camera_sequence_file(camera_sequence_file):
    parsed = CameraSequenceFile.parse(camera_sequence_file)
    built = CameraSequenceFile.build(parsed)

    assert camera_sequence_file == built
    assert parsed == CameraSequenceFile.parse(built)
