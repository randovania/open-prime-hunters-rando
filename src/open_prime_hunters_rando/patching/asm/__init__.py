import struct
from pathlib import Path

patch_files = Path(__file__).parent.parent.parent.joinpath("files", "asm_patches")

NOP = bytes.fromhex("00F020E3")


def read_bytes_from_file(asm_patch: str) -> bytes:
    return patch_files.joinpath(asm_patch).read_bytes()


def bitfield_to_bytes(bitfield: str | list) -> bytes:
    if isinstance(bitfield, list):
        fmt = "{:d}" * 8
        bitfield = fmt.format(*bitfield)
    return int(bitfield, 2).to_bytes()


class GenerateArmBytes:
    def __init__(self, in_game_value: int, multiply: bool = True) -> None:
        self.in_game_value = in_game_value
        self.multiply = multiply

    # This function was generated by Gemini
    def _set_instruction_bytes(self, destination_register: int, operand_register: int | None) -> bytes:
        # Calculate value and cap at 1020
        value = min(self.in_game_value * (10 if self.multiply else 1), 1020)

        # Determine the rotation logic
        if value > 255:
            # If divisible by 16 (like 400), use Field E
            is_mod_16 = value % 16 == 0
            rotate_field = 0xE if is_mod_16 else 0xF
            imm8 = value // (16 if is_mod_16 else 4)
        else:
            # No rotation needed for 0-255
            rotate_field = 0
            imm8 = value

        # Assemble the byte sequence
        result = struct.pack("<B", imm8)
        result += bytes.fromhex(f"{destination_register}{rotate_field:X}")

        # Handle optional operand register (e.g., for ADD)
        if operand_register is not None:
            result += bytes.fromhex(f"8{operand_register}")

        return result

    def add(self, destination_register: int, operand_register: int) -> bytes:
        """
        Generates a 4-byte ARM 'ADD Rd, Rn, #imm' instruction in Little Endian. Supports a max value of 1020.
        """
        # Set the instruction bytes
        instruction_bytes = self._set_instruction_bytes(destination_register, operand_register)

        return instruction_bytes + b"\xe2"

    def mov(self, destination_register: int) -> bytes:
        """
        Generates a 4-byte ARM 'MOV Rd, #imm' instruction in Little Endian. Supports a max value of 1020.
        """
        # Set the instruction bytes
        instruction_bytes = self._set_instruction_bytes(destination_register, None)

        return instruction_bytes + b"\xa0\xe3"
